		var a, b, sum;
		
		if (month <= 2) {
			year -= 1;
			month += 12;
		}
		
		sum = (year * 10000) + (month * 100) + date;
		a = Math.floor(year / 100);
		
		if (sum >= 15821015) {
			b = 2 - a + Math.floor(a / 4);
		} else if (sum <= 15821004) {
			b = 0;
		} else {
			return -1;
		}
		
		return Math.floor(365.25 * (year + 4716)) + Math.floor(30.6001 * (month + 1)) + date + (b - 1524.5);
